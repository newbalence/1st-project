package push;

public class PushVO {
	private String pushno;
	private String user;
	private String bno;
	
	public String getPushno() {
		return pushno;
	}
	public String getUser() {
		return user;
	}
	public String getBno() {
		return bno;
	}
	public void setPushno(String pushno) {
		this.pushno = pushno;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public void setBno(String bno) {
		this.bno = bno;
	}
	
}
