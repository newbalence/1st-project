package hit;

public class HitVO {
	private String hitno;
	private String user;
	private String bno;
	
	public String getHitno() {
		return hitno;
	}
	public String getUser() {
		return user;
	}
	public String getBno() {
		return bno;
	}
	public void setHitno(String hitno) {
		this.hitno = hitno;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public void setBno(String bno) {
		this.bno = bno;
	}
	
}
